package com.pro.domain;

public class TabState {

	private int state_id;//餐台状态编号
	
	private String tab_state;//餐台状态

	public int getState_id() {
		return state_id;
	}

	public void setState_id(int stateId) {
		state_id = stateId;
	}

	public String getTab_state() {
		return tab_state;
	}

	public void setTab_state(String tabState) {
		tab_state = tabState;
	}

}
